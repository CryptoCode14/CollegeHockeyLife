// New: Function for stat bounds and compounding debuffs
export function applyStatBounds(gameState) {
    const s = gameState.player.status;
    const a = gameState.player.attributes;
    s.energy = Math.max(0, Math.min(100, s.energy));
    s.stress = Math.max(0, Math.min(100, s.stress));
    s.reputation = Math.max(0, Math.min(100, s.reputation));
    s.gpa = Math.max(0, Math.min(4.0, s.gpa));
    s.happiness = Math.max(0, Math.min(100, s.happiness));
    s.health = Math.max(0, Math.min(100, s.health));
    s.injury = Math.max(0, Math.min(100, s.injury));
    s.nutrition = Math.max(0, Math.min(100, s.nutrition));
    s.mentalHealth = Math.max(0, Math.min(100, s.mentalHealth));

    // Compounding debuffs
    if (s.stress > 80) {
        a.skating -= 5; a.shooting -= 5; a.puckHandling -= 5; a.checking -= 5; a.defense -= 5; a.strength -= 5; a.speed -= 5; a.stamina -= 5; a.agility -= 5; a.awareness -= 5; // Lower performance
        s.happiness -= 2;
        s.mentalHealth -= 3;
    }
    if (s.injury > 50) {
        s.energy -= 20; // Compounding injury
        a.skating -= 10; a.speed -= 10; a.agility -= 10;
    }
    if (s.health < 50) {
        s.energy -= 10; 
        s.nutrition -= 5;
        s.mentalHealth -= 5;
    }
    if (s.nutrition < 50) {
        s.energy -= 5;
        s.health -= 3;
    }
    if (s.gpa < 2.0) {
        s.reputation -= 5; // Academic probation
        s.stress += 10;
        s.mentalHealth -= 5;
    }
    if (s.mentalHealth < 50) {
        s.stress += 10;
        s.happiness -= 5;
        s.reputation -= 3;
    }
    // Cap attributes
    Object.keys(a).forEach(key => a[key] = Math.max(0, Math.min(100, a[key])));

    // New: Money bounds
    gameState.player.money = Math.max(0, gameState.player.money);
}